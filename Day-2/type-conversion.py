# Type checking:
# ------------------------------------------------------------------------------------------------------------------------------------------
my_str = "Hello"
print(type(my_str))

my_num = 10
print(type(my_num))

my_float = 3.14159
print(type(my_float))

my_bool = True
print(type(my_bool))
# ------------------------------------------------------------------------------------------------------------------------------------------


# Type Conversion:
# ------------------------------------------------------------------------------------------------------------------------------------------
age = int(input("Enter your age : "))
print(age + " " + type(age))
# ------------------------------------------------------------------------------------------------------------------------------------------

