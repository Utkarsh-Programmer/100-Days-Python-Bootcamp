# Slicing:

alphabets = ['a', 'b', 'c', 'd', 'e', 'f', 'g']

print(alphabets[:])
print(alphabets[0:4])
print(alphabets[-5:])
print(alphabets[-5:-1])
print(alphabets[::-1])
print(alphabets[::-2])
